create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nickname text not null check (char_length(nickname) between 1 and 24),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.puzzle_bank (
  id bigint generated by default as identity primary key,
  puzzle_key text not null unique,
  numbers int[] not null check (array_length(numbers, 1) = 4),
  created_at timestamptz not null default now()
);

create table if not exists public.daily_events (
  id uuid primary key default gen_random_uuid(),
  event_date date not null unique,
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  created_at timestamptz not null default now(),
  check (ends_at > starts_at)
);

create table if not exists public.event_puzzles (
  event_id uuid not null references public.daily_events(id) on delete cascade,
  puzzle_index int not null check (puzzle_index between 1 and 10),
  puzzle_id bigint not null references public.puzzle_bank(id),
  primary key (event_id, puzzle_index),
  unique (event_id, puzzle_id)
);

create table if not exists public.runs (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  event_id uuid not null references public.daily_events(id) on delete cascade,
  started_at timestamptz not null default now(),
  completed_at timestamptz,
  score_ms bigint,
  run_status text not null default 'active' check (run_status in ('active', 'completed')),
  unique (user_id, event_id),
  check (score_ms is null or score_ms >= 0),
  check (
    (run_status = 'active' and completed_at is null and score_ms is null)
    or (run_status = 'completed' and completed_at is not null and score_ms is not null)
  )
);

create table if not exists public.run_solutions (
  run_id uuid not null references public.runs(id) on delete cascade,
  puzzle_index int not null check (puzzle_index between 1 and 10),
  steps jsonb not null,
  submitted_at timestamptz not null default now(),
  primary key (run_id, puzzle_index)
);

