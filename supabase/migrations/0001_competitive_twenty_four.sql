create or replace function public.today_in_shanghai()
returns date
language sql
stable
as $$
  select (now() at time zone 'Asia/Shanghai')::date;
$$;

create or replace function public.gcd_bigint(a bigint, b bigint)
returns bigint
language plpgsql
immutable
strict
set search_path = public
as $$
declare
  x bigint := abs(a);
  y bigint := abs(b);
  t bigint;
begin
  while y <> 0 loop
    t := x % y;
    x := y;
    y := t;
  end loop;

  return greatest(x, 1);
end;
$$;

create or replace function public.validate_24_solution(
  p_numbers int[],
  p_steps jsonb
)
returns boolean
language plpgsql
immutable
set search_path = public
as $$
declare
  v_ids text[] := array['n0', 'n1', 'n2', 'n3'];
  v_nums bigint[] := array[p_numbers[1]::bigint, p_numbers[2]::bigint, p_numbers[3]::bigint, p_numbers[4]::bigint];
  v_dens bigint[] := array[1::bigint, 1::bigint, 1::bigint, 1::bigint];
  v_new_ids text[];
  v_new_nums bigint[];
  v_new_dens bigint[];
  v_i int;
  v_j int;
  v_step jsonb;
  v_left_id text;
  v_right_id text;
  v_result_id text;
  v_operator_code text;
  v_left_pos int;
  v_right_pos int;
  v_num bigint;
  v_den bigint;
  v_gcd bigint;
begin
  if p_numbers is null
    or array_length(p_numbers, 1) <> 4
    or p_steps is null
    or jsonb_typeof(p_steps) <> 'array'
    or jsonb_array_length(p_steps) <> 3
  then
    return false;
  end if;

  for v_i in 0..2 loop
    v_step := p_steps -> v_i;

    if jsonb_typeof(v_step) <> 'object' then
      return false;
    end if;

    v_left_id := v_step ->> 'left_id';
    v_right_id := v_step ->> 'right_id';
    v_operator_code := v_step ->> 'operator_code';
    v_result_id := v_step ->> 'result_id';

    if v_result_id <> ('r' || v_i::text) then
      return false;
    end if;

    v_left_pos := array_position(v_ids, v_left_id);
    v_right_pos := array_position(v_ids, v_right_id);

    if v_left_pos is null or v_right_pos is null or v_left_pos = v_right_pos then
      return false;
    end if;

    case v_operator_code
      when '+' then
        v_num := v_nums[v_left_pos] * v_dens[v_right_pos]
          + v_nums[v_right_pos] * v_dens[v_left_pos];
        v_den := v_dens[v_left_pos] * v_dens[v_right_pos];
      when '-' then
        v_num := v_nums[v_left_pos] * v_dens[v_right_pos]
          - v_nums[v_right_pos] * v_dens[v_left_pos];
        v_den := v_dens[v_left_pos] * v_dens[v_right_pos];
      when '*' then
        v_num := v_nums[v_left_pos] * v_nums[v_right_pos];
        v_den := v_dens[v_left_pos] * v_dens[v_right_pos];
      when '/' then
        if v_nums[v_right_pos] = 0 then
          return false;
        end if;
        v_num := v_nums[v_left_pos] * v_dens[v_right_pos];
        v_den := v_dens[v_left_pos] * v_nums[v_right_pos];
      else
        return false;
    end case;

    if v_den = 0 then
      return false;
    end if;

    if v_den < 0 then
      v_num := -v_num;
      v_den := -v_den;
    end if;

    v_gcd := public.gcd_bigint(v_num, v_den);
    v_num := v_num / v_gcd;
    v_den := v_den / v_gcd;

    v_new_ids := array[]::text[];
    v_new_nums := array[]::bigint[];
    v_new_dens := array[]::bigint[];

    for v_j in 1..array_length(v_ids, 1) loop
      if v_j <> v_left_pos and v_j <> v_right_pos then
        v_new_ids := array_append(v_new_ids, v_ids[v_j]);
        v_new_nums := array_append(v_new_nums, v_nums[v_j]);
        v_new_dens := array_append(v_new_dens, v_dens[v_j]);
      end if;
    end loop;

    v_ids := array_append(v_new_ids, v_result_id);
    v_nums := array_append(v_new_nums, v_num);
    v_dens := array_append(v_new_dens, v_den);
  end loop;

  return array_length(v_ids, 1) = 1 and v_nums[1] = 24 * v_dens[1];
exception
  when others then
    return false;
end;
$$;

create or replace function public.ensure_daily_event(p_event_date date default public.today_in_shanghai())
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_event_id uuid;
begin
  insert into public.daily_events (event_date, starts_at, ends_at)
  values (
    p_event_date,
    p_event_date::timestamp at time zone 'Asia/Shanghai',
    (p_event_date + 1)::timestamp at time zone 'Asia/Shanghai'
  )
  on conflict (event_date) do update
  set event_date = excluded.event_date
  returning id into v_event_id;

  if (select count(*) from public.event_puzzles where event_id = v_event_id) < 10 then
    insert into public.event_puzzles (event_id, puzzle_index, puzzle_id)
    select
      v_event_id,
      (row_number() over (order by md5(p_event_date::text || ':' || pb.id::text)))::int,
      pb.id
    from public.puzzle_bank pb
    order by md5(p_event_date::text || ':' || pb.id::text)
    limit 10
    on conflict do nothing;
  end if;

  if (select count(*) from public.event_puzzles where event_id = v_event_id) < 10 then
    raise exception 'Puzzle bank must contain at least 10 puzzles';
  end if;

  return v_event_id;
end;
$$;

create or replace function public.build_run_state(p_run_id uuid)
returns jsonb
language plpgsql
security definer
stable
set search_path = public
as $$
declare
  v_state jsonb;
begin
  select jsonb_build_object(
    'run_id', r.id,
    'event_date', e.event_date,
    'started_at', r.started_at,
    'completed_at', r.completed_at,
    'score_ms', r.score_ms,
    'puzzles', coalesce(
      jsonb_agg(
        jsonb_build_object(
          'puzzle_index', ep.puzzle_index,
          'numbers', to_jsonb(pb.numbers),
          'solved', rs.run_id is not null
        )
        order by ep.puzzle_index
      ),
      '[]'::jsonb
    )
  )
  into v_state
  from public.runs r
  join public.daily_events e on e.id = r.event_id
  join public.event_puzzles ep on ep.event_id = e.id
  join public.puzzle_bank pb on pb.id = ep.puzzle_id
  left join public.run_solutions rs
    on rs.run_id = r.id
   and rs.puzzle_index = ep.puzzle_index
  where r.id = p_run_id
  group by r.id, e.event_date, r.started_at, r.completed_at, r.score_ms;

  return v_state;
end;
$$;

create or replace function public.set_profile(p_nickname text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_nickname text := left(nullif(trim(p_nickname), ''), 24);
begin
  if v_user_id is null then
    raise exception 'Authentication required';
  end if;

  if v_nickname is null then
    raise exception 'Nickname cannot be empty';
  end if;

  insert into public.profiles (id, nickname)
  values (v_user_id, v_nickname)
  on conflict (id) do update
  set nickname = excluded.nickname,
      updated_at = now();

  return jsonb_build_object('id', v_user_id, 'nickname', v_nickname);
end;
$$;

create or replace function public.start_daily_run()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_event_id uuid;
  v_run_id uuid;
begin
  if v_user_id is null then
    raise exception 'Authentication required';
  end if;

  insert into public.profiles (id, nickname)
  values (v_user_id, 'Player ' || left(v_user_id::text, 4))
  on conflict (id) do nothing;

  v_event_id := public.ensure_daily_event(public.today_in_shanghai());

  insert into public.runs (user_id, event_id)
  values (v_user_id, v_event_id)
  on conflict (user_id, event_id) do update
  set event_id = excluded.event_id
  returning id into v_run_id;

  return public.build_run_state(v_run_id);
end;
$$;

create or replace function public.resume_daily_run()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_event_id uuid;
  v_run_id uuid;
begin
  if v_user_id is null then
    return null;
  end if;

  v_event_id := public.ensure_daily_event(public.today_in_shanghai());

  select id
  into v_run_id
  from public.runs
  where user_id = v_user_id
    and event_id = v_event_id
  limit 1;

  if v_run_id is null then
    return null;
  end if;

  return public.build_run_state(v_run_id);
end;
$$;

create or replace function public.submit_solution(
  p_run_id uuid,
  p_puzzle_index int,
  p_steps jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_run public.runs%rowtype;
  v_event_date date;
  v_numbers int[];
  v_solved_count int;
begin
  if v_user_id is null then
    raise exception 'Authentication required';
  end if;

  select *
  into v_run
  from public.runs
  where id = p_run_id
    and user_id = v_user_id
  for update;

  if v_run.id is null then
    raise exception 'Run not found';
  end if;

  select event_date
  into v_event_date
  from public.daily_events
  where id = v_run.event_id;

  if v_event_date <> public.today_in_shanghai() then
    raise exception 'Only today event can accept submissions';
  end if;

  if v_run.run_status = 'completed' then
    return public.build_run_state(v_run.id);
  end if;

  select pb.numbers
  into v_numbers
  from public.event_puzzles ep
  join public.puzzle_bank pb on pb.id = ep.puzzle_id
  where ep.event_id = v_run.event_id
    and ep.puzzle_index = p_puzzle_index;

  if v_numbers is null then
    raise exception 'Puzzle not found';
  end if;

  if exists (
    select 1
    from public.run_solutions
    where run_id = v_run.id
      and puzzle_index = p_puzzle_index
  ) then
    return public.build_run_state(v_run.id);
  end if;

  if not public.validate_24_solution(v_numbers, p_steps) then
    raise exception 'Invalid solution';
  end if;

  insert into public.run_solutions (run_id, puzzle_index, steps)
  values (v_run.id, p_puzzle_index, p_steps);

  select count(*)
  into v_solved_count
  from public.run_solutions
  where run_id = v_run.id;

  if v_solved_count >= 10 then
    update public.runs
    set run_status = 'completed',
        completed_at = now(),
        score_ms = greatest(
          0,
          floor(extract(epoch from (now() - started_at)) * 1000)::bigint
        )
    where id = v_run.id
      and run_status <> 'completed';
  end if;

  return public.build_run_state(v_run.id);
end;
$$;

create or replace function public.get_today_leaderboard()
returns table (
  rank_position int,
  nickname text,
  score_ms bigint,
  completed_at timestamptz
)
language sql
security definer
stable
set search_path = public
as $$
  with today_event as (
    select id
    from public.daily_events
    where event_date = public.today_in_shanghai()
  ),
  ranked as (
    select
      (row_number() over (order by r.score_ms asc, r.completed_at asc, r.user_id asc))::int as rank_position,
      coalesce(p.nickname, 'Player') as nickname,
      r.score_ms,
      r.completed_at
    from public.runs r
    left join public.profiles p on p.id = r.user_id
    where r.event_id = (select id from today_event)
      and r.run_status = 'completed'
      and r.score_ms is not null
  )
  select rank_position, nickname, score_ms, completed_at
  from ranked
  order by rank_position
  limit 100;
$$;

create or replace function public.get_history_leaderboard()
returns table (
  rank_position int,
  nickname text,
  total_points bigint,
  completed_days bigint,
  best_score_ms bigint,
  last_completed_at timestamptz
)
language sql
security definer
stable
set search_path = public
as $$
  with event_ranks as (
    select
      r.user_id,
      r.score_ms,
      r.completed_at,
      (row_number() over (
        partition by r.event_id
        order by r.score_ms asc, r.completed_at asc, r.user_id asc
      ))::int as event_rank
    from public.runs r
    where r.run_status = 'completed'
      and r.score_ms is not null
  ),
  scored as (
    select
      user_id,
      case event_rank
        when 1 then 25
        when 2 then 18
        when 3 then 15
        when 4 then 12
        when 5 then 10
        when 6 then 8
        when 7 then 6
        when 8 then 4
        when 9 then 2
        else 1
      end as points,
      score_ms,
      completed_at
    from event_ranks
  ),
  grouped as (
    select
      user_id,
      sum(points)::bigint as total_points,
      count(*)::bigint as completed_days,
      min(score_ms)::bigint as best_score_ms,
      max(completed_at) as last_completed_at
    from scored
    group by user_id
  ),
  ranked as (
    select
      (row_number() over (
        order by
          g.total_points desc,
          g.completed_days desc,
          g.best_score_ms asc,
          g.last_completed_at asc,
          g.user_id asc
      ))::int as rank_position,
      coalesce(p.nickname, 'Player') as nickname,
      g.total_points,
      g.completed_days,
      g.best_score_ms,
      g.last_completed_at
    from grouped g
    left join public.profiles p on p.id = g.user_id
  )
  select rank_position, nickname, total_points, completed_days, best_score_ms, last_completed_at
  from ranked
  order by rank_position
  limit 100;
$$;

